package com.creditharmony.fortune.trusteeship.entity;

public class ChangeCount {
	
	private int cardChangeCount;// 银行卡变更数目
	private int phoneChangeCount;// 手机变更数目
	public int getCardChangeCount() {
		return cardChangeCount;
	}
	public void setCardChangeCount(int cardChangeCount) {
		this.cardChangeCount = cardChangeCount;
	}
	public int getPhoneChangeCount() {
		return phoneChangeCount;
	}
	public void setPhoneChangeCount(int phoneChangeCount) {
		this.phoneChangeCount = phoneChangeCount;
	}
	
	

}
